'''
Python = Linguagem de programação de alto nível, interpretada, interativa e orientada a objetos.
Tipo de tipagem = Dinâmica e forte.
str -> string -> texto
Strings são textos delimitados por aspas simples ou duplas.

'''
# Aspas simples
print('Hello, World!')

# Aspas duplas
print("Hello, World!")

# Aspas simples dentro de aspas duplas
print("Hello, I'm Estevão")

# Aspas duplas dentro de aspas simples
print('Hello, I am "Estevão"')

# Escape
print("Hello, Eu sou o \"Estevão\"")

# r = raw string = string crua
print(r"Hello, Eu sou o \"Estevão\"")

# Multiplas linhas
print('''Hello,
Eu sou o Estevão''')
